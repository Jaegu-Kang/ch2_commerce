public class Customer {
    // Customer 속성 변수를 선언합니다.(고객의 이름, 주소, 등급)
    public String name;
    public String email;
    public String grade;

    // Customer 생성자를 지정합니다.
    public Customer(String name, String email, String grade) {
        this.name = name;
        this.email = email;
        this.grade = grade;
    }

}
